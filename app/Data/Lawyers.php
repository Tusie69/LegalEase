    public static function findBySlug(string $slug): ?array
    {
        foreach (self::all() as $lawyer) {
            if ($lawyer['slug'] === $slug) {
                return $lawyer;
            }
        }
        return null;
    }

    public static function featured(int $count = 3): array
    {
        return array_slice(self::all(), 0, $count);
    }
}
